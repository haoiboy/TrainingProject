package Class04;

public class AnimalParent {
    public String name;
    public int age;

    public AnimalParent() {

    }
    public AnimalParent(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
